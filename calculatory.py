# 기본 계산기
def add(first, second):
    return first+second

def subtract(a,b):
    return a-b

def multiply(a,b):
    return a*b


def divide_new(a, b):
    return a/b

def get_Median(a, b):
    return (a+b)/2

def get_Remainder(a, b):
    return a//b

def get_Abs(num):
    if num>=0:
        return num
    else:
        return -num

